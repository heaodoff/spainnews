    try:
        response = client.chat.completions.create(
            model=VIRAL_MODEL,
            messages=[{"role": "user", "content": prompt}],
            temperature=0.7,
            max_tokens=3500,
        )

        text = response.choices[0].message.content.strip()
        logger.debug("Viral post AI response:\n%s", text[:500])

        # Parse HEADLINE
        headline_m = re.search(r"HEADLINE:\s*(.+)", text)
        headline = headline_m.group(1).strip() if headline_m else topic

        # Parse IMAGE
        image_m = re.search(r"IMAGE:\s*(.+?)(?:\n|===)", text, re.DOTALL)
        image_description = image_m.group(1).strip() if image_m else ""

        # Parse POST
        post_text = ""
        if "===POST===" in text:
            post_part = text.split("===POST===")[1]
            if "===COMMENT===" in post_part:
                post_text = post_part.split("===COMMENT===")[0].strip()
            else:
                post_text = post_part.strip()

        # Parse COMMENT
        comment_text = None
        if "===COMMENT===" in text:
            comment_text = text.split("===COMMENT===")[1].strip()

        if not post_text:
            logger.warning("No POST content generated for viral topic: %s", topic)
            return None

        logger.info("Generated viral post: %s", headline[:60])

        return {
            "format": "FULL",
            "short_post": post_text,
            "detailed_comment": comment_text,
            "image_headline": headline,
            "image_description": image_description,
            "category": category,
            "source": "",
            "url": "",
            "title": headline,
            "score": 5,
            "is_viral": True,
        }

    except Exception:
        logger.exception("Error generating viral post for topic: %s", topic)
        return None
